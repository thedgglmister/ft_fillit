#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int j;

	i = ft_strlen(s1);
	j = 0;
	while (n)
	{
		if (s2[j])
		{
			s1[i] = s2[j];
			j++;
			i++;
		}
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
